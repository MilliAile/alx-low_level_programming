#include "3-calc.h"
/**
 * op_add - return sum
 * @a: num1
 * @b: num2
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - return subtraction
 * @a: num1
 * @b: num2
 * Return: subtraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - return product
 * @a: num1
 * @b: num2
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - return division
 * @a: num1
 * @b: num2
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - return remainder
 * @a: num1
 * @b: num2
 * Return: division
 */
int op_mod(int a, int b)
{
	return (a % b);
}
